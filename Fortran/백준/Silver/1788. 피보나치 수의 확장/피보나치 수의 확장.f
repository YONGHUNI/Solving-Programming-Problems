INTEGER::A(1000001)
READ*,N
M=0
IF(N>0)M=1
IF(N<0)M=-1
N=ABS(N)
A(1+0)=0
A(1+1)=1
I=2
DO WHILE(I<=N)
A(1+I)=MOD(A(1+I-1)+A(1+I-2),1000000000)
I=I+1
ENDDO
IF(M==1)then
PRINT'(I0)',M
PRINT'(I0)',A(1+N)
ELSE IF(N==0)THEN
PRINT'(I0)',0
PRINT'(I0)',0
ELSE IF(N/=0.AND.M==-1)THEN
IF(MOD(N,2)==1)M=1
PRINT'(I0)',M
PRINT'(I0)',A(1+N)
ENDIF
END